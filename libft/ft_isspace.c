#include "libft.h"

int	ft_isspace(int c)
{
	return (ft_isblank(c) || \
			(unsigned)c == '\n' || \
			(unsigned)c == '\v' || \
			(unsigned)c == '\f' || \
			(unsigned)c == '\r');
}
